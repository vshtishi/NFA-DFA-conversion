public class AFD_State {

    private String state;
    private int status;

    public void setState(String state) {
        this.state = state;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getState() {
        return this.state;
    }

    public int getStatus() {
        return this.status;
    }
}
